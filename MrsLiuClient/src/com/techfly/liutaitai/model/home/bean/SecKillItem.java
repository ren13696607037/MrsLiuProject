package com.techfly.liutaitai.model.home.bean;

public class SecKillItem {

	private int mId;
	private int mCommodityId;
	private String mName;
	private double mPrice;
	private double mOldPrice;
	private String mImage;
	private boolean mEnabled;
	
	public int getmId() {
		return mId;
	}
	public void setmId(int mId) {
		this.mId = mId;
	}
	public int getmCommodityId() {
		return mCommodityId;
	}
	public void setmCommodityId(int mCommodityId) {
		this.mCommodityId = mCommodityId;
	}
	public String getmName() {
		return mName;
	}
	public void setmName(String mName) {
		this.mName = mName;
	}
	public double getmPrice() {
		return mPrice;
	}
	public void setmPrice(double mPrice) {
		this.mPrice = mPrice;
	}
	public double getmOldPrice() {
		return mOldPrice;
	}
	public void setmOldPrice(double mOldPrice) {
		this.mOldPrice = mOldPrice;
	}
	public String getmImage() {
		return mImage;
	}
	public void setmImage(String mImage) {
		this.mImage = mImage;
	}
	public boolean ismEnabled() {
		return mEnabled;
	}
	public void setmEnabled(boolean mEnabled) {
		this.mEnabled = mEnabled;
	}
	
	
}
