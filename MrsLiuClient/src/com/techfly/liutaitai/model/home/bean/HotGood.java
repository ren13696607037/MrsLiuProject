package com.techfly.liutaitai.model.home.bean;

public class HotGood {

	private int mId;
	private int mCommodityId;
	private String mCommodityName;
	private String mDescription;
	private String mImage;
	private double mMarketPrice;
	private double mPrice;
	
	public int getmId() {
		return mId;
	}
	public void setmId(int mId) {
		this.mId = mId;
	}
	public int getmCommodityId() {
		return mCommodityId;
	}
	public void setmCommodityId(int mCommodityId) {
		this.mCommodityId = mCommodityId;
	}
	public String getmCommodityName() {
		return mCommodityName;
	}
	public void setmCommodityName(String mCommodityName) {
		this.mCommodityName = mCommodityName;
	}
	public String getmDescription() {
		return mDescription;
	}
	public void setmDescription(String mDescription) {
		this.mDescription = mDescription;
	}
	public String getmImage() {
		return mImage;
	}
	public void setmImage(String mImage) {
		this.mImage = mImage;
	}
	public double getmMarketPrice() {
		return mMarketPrice;
	}
	public void setmMarketPrice(double mMarketPrice) {
		this.mMarketPrice = mMarketPrice;
	}
	public double getmPrice() {
		return mPrice;
	}
	public void setmPrice(double mPrice) {
		this.mPrice = mPrice;
	}
	
	
}
