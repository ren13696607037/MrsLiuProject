package com.techfly.liutaitai.model.mall.bean;

public class ServiceInfo {
    private String mImg;
    private String mName;
    private String mServiceConent;
    private String mDesc;
    private String mId;
    private String mPrice;
    private String mUnit;
    private String mType;

    public String getmPrice() {
        return mPrice;
    }

    public void setmPrice(String mPrice) {
        this.mPrice = mPrice;
    }

    public String getmUnit() {
        return mUnit;
    }

    public void setmUnit(String mUnit) {
        this.mUnit = mUnit;
    }

    public String getmImg() {
        return mImg;
    }

    public void setmImg(String mImg) {
        this.mImg = mImg;
    }

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmServiceConent() {
        return mServiceConent;
    }

    public void setmServiceConent(String mServiceConent) {
        this.mServiceConent = mServiceConent;
    }

    public String getmDesc() {
        return mDesc;
    }

    public void setmDesc(String mDesc) {
        this.mDesc = mDesc;
    }

    public String getmId() {
        return mId;
    }

    public void setmId(String mId) {
        this.mId = mId;
    }

    public String getmType() {
        return mType;
    }

    public void setmType(String mType) {
        this.mType = mType;
    }
}
